letters = 'abcdefghijklmnopqrstuvwxyz' # python array
print(letters[0]) #print first char - a

print(letters[-1]) #print last char - z

print(letters [-2]) #print second from the last - y

print(letters[:]) #print the entire string

print('Na' *4) #wil print NaNaNaNa - four times

#slice

print(letters[:-1]) #from beginning to end offset minus 1 a-y

print(letters[0:-2]) #from start to end offset minus 1 - a-x

print(letters[0:5:2]) #display ace, start to end skipping one char

print(letters[-6:-2]) #display u-x

